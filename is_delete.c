#include "ft_select.h"

int	is_delete(char *buff)
{
	if (buff[0] == ESC
		&& buff[1] == ARROW
		&& buff[2] == 0x33
		&& buff[3] == 0x7e)
		return (TRUE);
	return (FALSE);
}
